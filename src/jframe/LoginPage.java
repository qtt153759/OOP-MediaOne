    public boolean validateLogin(){
        String name = txt_username.getText();
        String pwd = txt_password1.getText();
        
        if(name.equals("") || name.equals("Nhập tài khoản ...")){
            JOptionPane.showMessageDialog(this, "Hãy nhập tài khoản");
            return false;
        }
        if(pwd.equals("") || pwd.equals("Nhập mật khẩu ...")){
            JOptionPane.showMessageDialog(this, "Hãy nhập mật khẩu");
            return false;
        }
        return true;
    }
    
    public void login(){
        String name = txt_username.getText();
        //String pwd = txt_password.getText();
        String pwd = txt_password1.getText();
        try{
            Class.forName("com.mysql.jdbc.Driver");
            Connection con = DriverManager.getConnection("jdbc:mysql://localhost:3306/MediaOne", "root", "");
            PreparedStatement pst = con.prepareStatement("select * from users where name = ? and password = ?");
            
            pst.setString(1, name);
            pst.setString(2, pwd);
            
            ResultSet rs = pst.executeQuery();
            if(rs.next()){
                JOptionPane.showMessageDialog(this, "Đăng nhập thành công");
                user_name = name;
                HomePage home = new HomePage();
                home.setVisible(true);
                this.dispose();
            }
            else{
                JOptionPane.showMessageDialog(this, "Sai tài khoản hoặc mật khẩu");
                
            }
        }catch(Exception e){
            e.printStackTrace();
        }
    }
    
  
   
    
